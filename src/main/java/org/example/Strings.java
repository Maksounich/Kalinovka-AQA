package org.example;

public class Strings {


    public static void main(String[] args) {

        String a = "Hello";
        String b = " ";
        String c = "Bob";
        String d = " and Mike";
        String e = "!";


        System.out.println(a + b + c + d + e);


        String k = "My age is ";
        int x = 10;

        System.out.println(k + x);


        String alpha = new String("i have ");
        String beta = new String(" apples");


        System.out.println(alpha + x + beta);

        String gamma = alpha + beta + e;

        System.out.println(gamma);


    }
}
